package ClassMethod;

import java.util.Scanner;


public class Calculator {
    public int addition(int aNumber, int bNumber) {

        return aNumber + bNumber;
    }
    public int substration(int aNumber, int bNumber) {
        return aNumber - bNumber;
    }
    public int multiplication(int aNumber, int bNumber) {
        return aNumber * bNumber;
    }
    public int division(int aNumber, int bNumber) {
        return aNumber / bNumber;
    }
    public int modulo(int aNumber, int bNumber) {
        return aNumber % bNumber;
    }
}